public class Book {
    private String name;
    private String edition;

    public Book(){
        name = "";
        edition = "";
    }

    public String getName() {
        return name;
    }

    public String getEdition() {
        return edition;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setEdition(String edition) {
        this.edition = edition;
    }
    
    
    
   
}
